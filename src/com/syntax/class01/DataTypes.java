package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {
		
		//storing whole numbers
		byte box1=127; //byte can storage range from-128 till 127
		short box2=1000; // short can storage range from from -32768 till 32767
		int box3=10000;  // most popular data type to use for whole numbers
		long box4=9900000000000000l;
		 
		 //representing and storing decimal numbers
		 
		 float a=1.99f; // 5 to 6 decimal places
		 double b=19.99; // 14 to 14 places of accure. most popular to use for decimal numbers
		 
		 // representing and storing single character
		 
		 char letter='#';
		 char character='c';

		// representing and storing boolean values (yes or no)
		 
		 boolean tired=true; // yes
		 boolean hungry=false; // no
		 
		 System.out.println(box1);
		 System.out.println(box2);
		 System.out.println(box3);
		 System.out.println(box4);
		 System.out.println(a);
		 System.out.println(b);
		 System.out.println(letter);
		 System.out.println(character);
		 System.out.println(tired);
		 System.out.println(hungry);
		 
		
	}

}
