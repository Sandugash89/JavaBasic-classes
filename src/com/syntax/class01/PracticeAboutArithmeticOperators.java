public class PracticeAboutArithmeticOperators {

	public static void main(String[] args) {
	/* 
	 * Addition ==> +
	 * Subtraction ==> -
	 * Multiplication ==> *
	 * Division ==> /
	 */
		int num1, num2, num3, num4, num5;
		int sum1, sum2, difference1, difference2, division1, division2, multiplication;
		
		num1=10;
		num2=15;
		num3=20;
		num4=30;
		num5=35;
		sum1=num1+5;
		difference1=num3-num2;
		sum2=num2+num3;
		difference2=num5-num4;
		division1=num4/num1;
		division2=num3/num2;
		multiplication=num1*num2;
		
		
	    System.out.println("num1 plus 5 =" + sum1);
	    System.out.print("num3 minus num2 =" + difference1 + "\n");
	    System.out.print("num2 plus num3 =" + sum2 +"\n");
	    System.out.print("num5 minus num4 =" + difference2 + "\n");
	    System.out.println("30/10 = " + division1);
	    System.out.print("num3 division num2 =" + division2 + "\n");
	    System.out.println("num1 multiplication num2 ="+ multiplication);
	    
	    
	    
	    
	

	}

}
